#include "../../include/get_next_line.h"

void	ft_bzero_1(void *s, size_t len)
{
	char	*str;
	size_t	i;

	str = (char *)s;
	i = 0;
	while (i < len)
	{
		str[i] = '\0';
		i++;
	}
}
